import itertools


# ─────────────────────────────────────────────
# Задача 1. Слова Васи
# ─────────────────────────────────────────────

def count_vasya_words():
    """
    Подсчёт 6-буквенных слов из алфавита {В, И, Ш, Н, Я}.

    Ограничения:
    - буква В встречается не более одного раза
    - слово не начинается с Ш
    - слово не оканчивается на гласную (И или Я)

    Использует itertools.product для перебора всех комбинаций.

    Returns:
        int: количество допустимых слов

    >>> count_vasya_words()
    4352
    """
    alphabet = ['В', 'И', 'Ш', 'Н', 'Я']
    vowels = {'И', 'Я'}
    count = 0
    for word in itertools.product(alphabet, repeat=6):
        if word[0] == 'Ш':
            continue
        if word[-1] in vowels:
            continue
        if word.count('В') > 1:
            continue
        count += 1
    return count


# ─────────────────────────────────────────────
# Задача 2. Единицы в двоичной записи
# ─────────────────────────────────────────────

def count_ones_in_binary():
    """
    Подсчёт единиц в двоичной записи выражения 4^2014 + 2^2015 - 8.

    Аналитическое объяснение:
      4^2014 = 2^4028          → единица на бите 4028
      2^2015 - 8 = 2^3 * (2^2012 - 1)
                               → биты 3..2014, все единицы (2012 штук)
      Итого: 1 + 2012 = 2013

    Returns:
        int: количество единиц в двоичной записи

    >>> count_ones_in_binary()
    2013
    """
    value = 4**2014 + 2**2015 - 8
    return bin(value).count('1')


# ─────────────────────────────────────────────
# Задача 3. Числа вида 2^m * 3^n
# ─────────────────────────────────────────────

def find_2m_3n_numbers():
    """
    Находит все натуральные числа N в отрезке [400_000_000; 600_000_000],
    представимые в виде N = 2^m * 3^n,
    где m — чётное (0, 2, 4, ...), n — нечётное (1, 3, 5, ...).

    Returns:
        list[int]: найденные числа в порядке возрастания

    >>> find_2m_3n_numbers()
    [408146688, 452984832, 516560652, 573308928]
    """
    lo, hi = 400_000_000, 600_000_000
    result = []

    m = 0
    while 2**m <= hi:
        if m % 2 == 0:
            base = 2**m
            n = 1
            while True:
                val = base * (3**n)
                if val > hi:
                    break
                if val >= lo and n % 2 == 1:
                    result.append(val)
                n += 1
        m += 1

    return sorted(result)


